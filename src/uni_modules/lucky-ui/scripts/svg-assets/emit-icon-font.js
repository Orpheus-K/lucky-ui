const fs = require('fs').promises;
const path = require('path');
const { moduleRoot, relativeToModule, resolveModulePath } = require('./path-utils');

async function ensureEmptyDir(dir) {
  await fs.mkdir(dir, { recursive: true });
  const files = await fs.readdir(dir);
  await Promise.all(
    files.map(file => fs.rm(path.join(dir, file), { recursive: true, force: true }))
  );
}

async function ensureParentDir(filePath) {
  await fs.mkdir(path.dirname(filePath), { recursive: true });
}

async function readExistingSvgNames(dir) {
  try {
    const files = await fs.readdir(dir);
    return files
      .filter(file => file.endsWith('.svg'))
      .map(file => path.basename(file, '.svg'))
      .sort();
  } catch {
    return [];
  }
}

function diffNames(previous, next) {
  const previousSet = new Set(previous);
  const nextSet = new Set(next);
  return {
    added: next.filter(name => !previousSet.has(name)),
    removed: previous.filter(name => !nextSet.has(name)),
  };
}

function escapeRegex(value) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function parseCodepointsFromCss(css, classNamePrefix = 'lk-icon') {
  const map = {};
  const re = new RegExp(
    `\\.${escapeRegex(classNamePrefix)}-([^:\\s]+)::before\\s*\\{\\s*content:\\s*"\\\\([0-9a-fA-F]+)"`,
    'g'
  );
  let match;

  while ((match = re.exec(css))) {
    map[match[1]] = parseInt(match[2], 16);
  }

  return map;
}

function generateCodepointsTs(map, sourceLabel = 'svg-assets') {
  return `// This file is generated by ${sourceLabel}.
export const ICON_CODEPOINTS: Record<string, number> = ${JSON.stringify(map, null, 2)} as const;

export type BuiltInIconName = keyof typeof ICON_CODEPOINTS;

export function iconCharOf(name: string): string {
  const cp = (ICON_CODEPOINTS as Record<string, number>)[name];
  return cp ? String.fromCodePoint(cp) : '';
}
`;
}

function createDefinitionsCss(css) {
  return css.replace(/@font-face\s*\{[\s\S]*?\}\s*/m, '').trimStart();
}

function createRuntimeFontFace(fontName, css) {
  const timestamp = css.match(/\?t=\d+/)?.[0] || '';
  return `@font-face {
  font-family: '${fontName}';
  src:
    url('${fontName}.woff2${timestamp}') format('woff2'),
    url('${fontName}.woff${timestamp}') format('woff');
}`;
}

function normalizeRuntimeFontFace(css, fontName) {
  return css.replace(/@font-face\s*\{[\s\S]*?\}\s*/m, `${createRuntimeFontFace(fontName, css)}\n\n`);
}

async function normalizeGeneratedFontStyles(fontsDir, fontName) {
  const styleFiles = [
    `${fontName}.css`,
    `${fontName}.scss`,
    `${fontName}.less`,
    `${fontName}.module.less`,
    `${fontName}.styl`,
  ];

  await Promise.all(
    styleFiles.map(async file => {
      const filePath = path.join(fontsDir, file);
      try {
        const css = await fs.readFile(filePath, 'utf8');
        await fs.writeFile(filePath, normalizeRuntimeFontFace(css, fontName), 'utf8');
      } catch (error) {
        if (error.code !== 'ENOENT') throw error;
      }
    })
  );
}

async function prepareIconFontAssets(assets, targetConfig, options = {}) {
  const outDir = resolveModulePath(targetConfig.outDir);
  const lockPath = resolveModulePath(targetConfig.lockFile);
  const iconAssets = assets.filter(asset => asset.target === 'iconFont');
  const writes = [];
  const nextNames = iconAssets.map(asset => asset.name).sort();
  const previousNames = await readExistingSvgNames(outDir);
  const changes = [
    {
      target: 'iconFont',
      added: diffNames(previousNames, nextNames).added,
      removed: diffNames(previousNames, nextNames).removed,
    },
  ];

  if (!options.check) {
    await ensureEmptyDir(outDir);
    for (const asset of iconAssets) {
      await fs.writeFile(path.join(outDir, `${asset.name}.svg`), asset.content, 'utf8');
    }

    const lock = {
      generatedAt: new Date().toISOString(),
      total: iconAssets.length,
      outDir: relativeToModule(outDir),
      items: nextNames,
      sources: Array.from(new Set(iconAssets.map(asset => asset.sourceId))).sort(),
    };
    await ensureParentDir(lockPath);
    await fs.writeFile(lockPath, `${JSON.stringify(lock, null, 2)}\n`, 'utf8');
  }

  writes.push({
    target: 'iconFont',
    path: relativeToModule(outDir),
    count: iconAssets.length,
    action: options.check ? 'planned' : 'written',
  });
  writes.push({
    target: 'iconFont',
    path: relativeToModule(lockPath),
    count: 1,
    action: options.check ? 'planned' : 'written',
  });

  return { writes, changes };
}

async function buildIconFont(targetConfig, options = {}) {
  const srcDir = resolveModulePath(targetConfig.outDir);
  const fontsDir = resolveModulePath(targetConfig.fontsDir);
  const writes = [];

  if (options.check) {
    return {
      writes: [
        {
          target: 'iconFont',
          path: relativeToModule(fontsDir),
          count: 1,
          action: 'planned',
        },
      ],
    };
  }

  const svgtofont = (await import('svgtofont')).default;
  await svgtofont({
    src: srcDir,
    dist: fontsDir,
    fontName: targetConfig.fontName,
    css: true,
    types: targetConfig.types || ['woff2', 'woff'],
    classNamePrefix: targetConfig.classNamePrefix || 'lk-icon',
    website: null,
    svgicons2svgfont: {
      normalize: true,
      fontHeight: 1000,
      descent: 0,
    },
  });

  const cssPath = path.join(fontsDir, `${targetConfig.fontName}.css`);
  const definitionsPath = path.join(fontsDir, `${targetConfig.fontName}-definitions.css`);
  const codepointsPath = resolveModulePath(targetConfig.codepointsFile);
  await normalizeGeneratedFontStyles(fontsDir, targetConfig.fontName);
  const css = await fs.readFile(cssPath, 'utf8');
  const codepoints = parseCodepointsFromCss(css, targetConfig.classNamePrefix);

  await fs.writeFile(definitionsPath, createDefinitionsCss(css), 'utf8');
  await ensureParentDir(codepointsPath);
  await fs.writeFile(codepointsPath, generateCodepointsTs(codepoints), 'utf8');

  writes.push({
    target: 'iconFont',
    path: relativeToModule(fontsDir),
    count: Object.keys(codepoints).length,
    action: 'written',
  });
  writes.push({
    target: 'iconFont',
    path: relativeToModule(codepointsPath),
    count: Object.keys(codepoints).length,
    action: 'written',
  });

  if (targetConfig.base64) {
    const result = await writeFontBase64(targetConfig.base64);
    writes.push(result);
  }

  return { writes };
}

async function writeFontBase64(base64Config) {
  const source = resolveModulePath(base64Config.source);
  const outFile = resolveModulePath(base64Config.outFile);
  const exportName = base64Config.exportName || 'LK_ICONS_WOFF_BASE64';
  const format = base64Config.format || 'woff';
  const buf = await fs.readFile(source);
  const ts = `// This file is generated from ${path.basename(source)}.
export const ${exportName} = '${buf.toString('base64')}';
export const LK_ICONS_FONT_FORMAT = '${format}';
`;

  await ensureParentDir(outFile);
  await fs.writeFile(outFile, ts, 'utf8');
  return {
    target: 'iconFont',
    path: relativeToModule(outFile),
    count: 1,
    action: 'written',
  };
}

module.exports = {
  buildIconFont,
  createDefinitionsCss,
  createRuntimeFontFace,
  generateCodepointsTs,
  normalizeRuntimeFontFace,
  parseCodepointsFromCss,
  prepareIconFontAssets,
  writeFontBase64,
  diffNames,
  moduleRoot,
};
